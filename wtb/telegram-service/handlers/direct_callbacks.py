    # –í–ê–ñ–ù–û: –ü—Ä–æ–≤–µ—Ä—è–µ–º, –±—ã–ª –ª–∏ –∫–æ–ª–±—ç–∫ —É–∂–µ –æ–±—Ä–∞–±–æ—Ç–∞–Ω middleware
    if getattr(callback_query, '_handled', False):
        logger.info("–ö–æ–ª–±—ç–∫ —É–∂–µ –æ–±—Ä–∞–±–æ—Ç–∞–Ω middleware, –ø—Ä–æ–ø—É—Å–∫–∞–µ–º")
