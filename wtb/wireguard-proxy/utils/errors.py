class ProxyError(Exception):
    """Базовый класс для всех ошибок прокси-сервера"""
    pass

class RemoteServerError(ProxyError):
    """
    Ошибка при взаимодействии с удаленным сервером WireGuard
    
    Используется при:
    - Недоступности удаленного сервера
    - Ошибках аутентификации
    - Ошибках выполнения запроса на удаленном сервере
    """
    pass

class NoAvailableServerError(ProxyError):
    """
    Ошибка при отсутствии доступных серверов
    
    Используется когда:
    - Нет серверов для указанной геолокации
    - Все серверы недоступны
    - Нет серверов с достаточными ресурсами
    """
    pass

class AuthenticationError(ProxyError):
    """
    Ошибка аутентификации
    
    Используется при:
    - Неверных учетных данных для удаленного сервера
    - Истечении срока действия токена
    - Отсутствии необходимых разрешений
    """
    pass

class ConfigurationError(ProxyError):
    """
    Ошибка конфигурации
    
    Используется при:
    - Неверной конфигурации прокси-сервера
    - Неверной конфигурации подключения к удаленному серверу
    - Отсутствии необходимых параметров конфигурации
    """
    pass

class DatabaseError(ProxyError):
    """
    Ошибка при взаимодействии с базой данных
    
    Используется при:
    - Недоступности сервиса базы данных
    - Ошибках выполнения запросов к базе данных
    - Неконсистентности данных
    """
    pass

class ValidationError(ProxyError):
    """
    Ошибка валидации данных
    
    Используется при:
    - Неверном формате данных в запросе
    - Отсутствии обязательных полей
    - Неверных значениях параметров
    """
    pass